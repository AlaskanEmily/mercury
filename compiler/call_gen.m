:- pred call_gen__input_args(list(arg_info), list(arg_loc)).
:- mode call_gen__input_args(in, out) is det.

call_gen__input_args([], []).
call_gen__input_args([arg_info(Loc, Mode)|Args], Vs) :-
	(
		Mode = top_in
	->
		Vs = [Loc|Vs0]
	;
		Vs = Vs0
	),
	call_gen__input_args(Args, Vs0).

